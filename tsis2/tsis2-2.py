#list is the one of four built-in data types(a data type that can store multiple items within itself(like arrays))
thislist = ["apple", "banana", "cherry"] #we use square brackets to make a list
print(thislist)

thislist = ["apple", "banana", "cherry", "apple", "cherry"] #allows duplicates
print(thislist)

thislist = ["apple", "banana", "cherry"]
print(len(thislist)) #this commands shows the number of elements in the list

#you can contain different types of data in list

mylist = ["apple", "banana", "cherry"]
